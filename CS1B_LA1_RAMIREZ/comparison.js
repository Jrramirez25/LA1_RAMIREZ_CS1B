// Declare two variables representing favorite numbers
let firstFavNumb = 7;
let secondFavNumb = 3;
// Compare the two numbers using different operators and log the results to the console
// Greater than operator
console.log("Greater than:", firstFavNumb > secondFavNumb); // true - 7 is greater than 3
// Less than operator
console.log("Less than:", firstFavNumb < secondFavNumb); // false - 7 is not less than 3
// Greater than or equal to operator
console.log("Greater than or equal to:", firstFavNumb >= secondFavNumb); // true - 7 is greater than or equal to 3
// Less than or equal to operator
console.log("Less than or equal to:", firstFavNumb <= secondFavNumb); // false - 7 is not less than or equal to 3
// Strict equality operator (checks both value and type)
console.log("Strict equality (===):", firstFavNumb === secondFavNumb); // false - 7 is not strictly equal to 3
// Loose equality operator (performs type coercion if necessary)
console.log("Loose equality (==):", firstFavNumb == secondFavNumb); // false - 7 is not loosely equal to 3
// Strict inequality operator (checks both value and type)
console.log("Strict inequality (!==):", firstFavNumb !== secondFavNumb); // true - 7 is not strictly equal to 3
// Loose inequality operator (performs type coercion if necessary)
console.log("Loose inequality (!=):", firstFavNumb != secondFavNumb); // true - 7 is not loosely equal to 3